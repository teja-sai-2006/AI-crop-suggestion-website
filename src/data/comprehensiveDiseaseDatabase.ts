// Helper functions for plant part detection
export const getDiseasesForPlantPart = (plantPart: string): ComprehensiveDisease[] => {
  return comprehensiveDiseaseDatabase.filter(disease => 
    disease.plantParts.includes(plantPart as any)
  );
};

export const getRandomDiseases = (plantParts: string[], count: number = 1): ComprehensiveDisease[] => {
  const relevantDiseases = comprehensiveDiseaseDatabase.filter(disease =>
    disease.plantParts.some(part => plantParts.includes(part))
  );
  
  if (relevantDiseases.length === 0) {
    // Fallback to all diseases if no specific match
    return getRandomItems(comprehensiveDiseaseDatabase, count);
  }
  
  return getRandomItems(relevantDiseases, count);
};

function getRandomItems<T>(array: T[], count: number): T[] {
  const shuffled = [...array].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}